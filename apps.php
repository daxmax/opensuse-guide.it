<div class="heading2">6.1 Naviga il Web con Firefox</div>
Mozilla Firefox è il web browser di default. E' uno dei migliori web browsers al mondo.<br /><br />

<center><a href="images/screenshots/firefox.png" rel="thumbnail"><img src="images/screenshots/firefoxb.png" alt="firefox" class="pic" /></a></center><br />

