<div class="heading2">6.2 LibreOffice, il software libero di produttività personale</div>
LibreOffice e una suite di programmi di produttività personale che comprende un word processor, un foglio di calcolo, un programma per presentazioni multimediali, un database, un programma di disegno e altro ancora. E' compatible con Microsoft Office.<br /><br />

<center><a href="images/screenshots/lowriter.png" rel="thumbnail"><img src="images/screenshots/lowriterb.png" alt="libreoffice" class="pic" /></a></center><br />

Puoi provare anche: Calligra, Abiword, Gnumeric<br /><br />



<div class="heading2">6.3 Ascolta la tua Musica con Amarok</div>
Amarok è un completo player musicale con cui puoi gestire la tua musica, i tuoi podcast, i tuoi lettori multimediali, iPod ecc. Leggi come abilitare i codec in un prossimo capitolo.<br /><br />

<center><a href="images/screenshots/amarok.png" rel="thumbnail"><img src="images/screenshots/amarokb.png" alt="amarok" class="pic" /></a></center><br />

Puoi provare anche: Clementine, JuK, Qmmp<br /><br />



<div class="heading2">6.4 Guarda i tuoi Video con Dragon Player</div>
Dragon Player visualizza file video e DVD. Leggi come abilitare i codec in un prossimo capitolo.<br /><br />

<center><a href="images/screenshots/dragon.png" rel="thumbnail"><img src="images/screenshots/dragonb.png" alt="vlc" class="pic" /></a></center><br />

Puoi provare anche: VLC, SMPlayer, mpv<br /><br />



