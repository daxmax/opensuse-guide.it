<div class="heading1">1. Introduzione</div>
Lo scopo di questa guida è di fornire ai nuovi utenti tutte le informazioni necessarie per iniziare ad usare openSUSE Leap come un sistema operativo casalingo e/o per uso di ufficio.<br /><br /> 

Leggere questa breve guida vi fornirà le informazioni basilari che vi permetteranno di risparmiare tempo in futuro. L’appendice è rivolta a utenti più avanzati.<br /><br />

N.B. Questa guida non è ufficiale e non è collegata all’<a href="http://www.opensuse.org" target="_blank">openSUSE project</a>,a <a href="http://www.suse.com" target="_blank">SUSE LINUX Products GmbH</a> e Micro Focus International.<br /><br />

Buona lettura!<br /><br />



<div class="heading2">1.1 openSUSE Leap</div>
openSUSE Leap è un sistema operativo per PC basato su GNU e Linux. E’ un’alternativa open source a Microsoft Windows che presenta molti vantaggi. openSUSE Leap è utilizzabile su computer laptop, desktop, notebook, server e PC multimediali utilizzati sia a casa che in ufficio.<br /><br />

OpenSUSE Leap è una distribuzione leader nel panorama GNU/Linux oltre ad essere una delle più longeve.<br />
openSUSE Leap utilizza il core del sistema SUSE Linux Enterprise aggiungendo un migliore supporto hardware, un migliore ambiente desktop e ricche applicazioni grafiche.<br/>
Il progetto prevede un aggiornamento principale della distribuzione ogni anno e, ogni 3-4 anni, verrà anche aggiornato il core basato su SUSE Linux Enterprise.<br /><br />

Gli utenti esperti e appassionati di GNU/Linux, che preferiscono utilizzare sempre le applicazioni più aggiornate, possono optare per la rolling release di openSUSE chiamata <a href="https://en.opensuse.org/Portal:Tumbleweed" target="_blank">openSUSE Tumbleweed</a>.<br /><br />

openSUSE Leap è sviluppata in maniera aperta e trasparente dai volontari della comunità che lavorano insieme agli impiegati SUSE.<br /><br />



